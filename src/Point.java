public class Point {
    private double x;
    private double y;
    
    // Constructor
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
    
    // Otros métodos según sea necesario
}
